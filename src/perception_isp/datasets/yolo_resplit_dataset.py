"""Create a matched YOLO train/val resplit from an existing YOLO export."""

from __future__ import annotations

import argparse
import json
import random
import re
import shutil
from dataclasses import dataclass
from pathlib import Path
from typing import Any, Dict, Mapping, Sequence

import numpy as np

from perception_isp.core.types import json_ready


_SAMPLE_ID_RE = re.compile(r"^\d+_(?P<sample>.+?)(?:_harddup\d+)?$")


@dataclass(frozen=True)
class YoloItem:
    sample_id: str
    stem: str
    source_split: str
    image_paths: tuple[Path, ...]
    label_path: Path


def main(argv: Sequence[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description="Resplit a YOLO dataset using sample IDs.")
    parser.add_argument("--source", required=True)
    parser.add_argument("--destination", required=True)
    parser.add_argument("--seed", type=int, default=101)
    parser.add_argument("--eval-fraction", type=float, default=0.25)
    parser.add_argument("--val-count", type=int, default=None)
    parser.add_argument("--split-manifest", default=None, help="Reuse a JSON split manifest generated by this tool.")
    parser.add_argument("--write-split-manifest", default=None)
    parser.add_argument("--hard-repeat", type=int, default=1, help="Total train copies for hard images, including original.")
    parser.add_argument("--small-area", type=float, default=0.02)
    parser.add_argument("--thin-aspect", type=float, default=3.0)
    parser.add_argument("--copy", action="store_true")
    parser.add_argument("--overwrite", action="store_true")
    args = parser.parse_args(argv)

    summary = create_resplit_dataset(
        source=Path(args.source),
        destination=Path(args.destination),
        seed=int(args.seed),
        eval_fraction=float(args.eval_fraction),
        val_count=args.val_count,
        split_manifest=Path(args.split_manifest) if args.split_manifest else None,
        write_split_manifest=Path(args.write_split_manifest) if args.write_split_manifest else None,
        hard_repeat=max(int(args.hard_repeat), 1),
        small_area=float(args.small_area),
        thin_aspect=float(args.thin_aspect),
        copy=bool(args.copy),
        overwrite=bool(args.overwrite),
    )
    print(json.dumps(json_ready(summary), indent=2))
    return 0


def create_resplit_dataset(
    *,
    source: Path,
    destination: Path,
    seed: int = 101,
    eval_fraction: float = 0.25,
    val_count: int | None = None,
    split_manifest: Path | None = None,
    write_split_manifest: Path | None = None,
    hard_repeat: int = 1,
    small_area: float = 0.02,
    thin_aspect: float = 3.0,
    copy: bool = False,
    overwrite: bool = False,
) -> Dict[str, Any]:
    source = source.expanduser().resolve()
    destination = destination.expanduser()
    if destination.exists():
        if not overwrite:
            raise FileExistsError(f"{destination} exists; pass --overwrite")
        shutil.rmtree(destination)
    destination.mkdir(parents=True, exist_ok=True)

    yaml_payload = _read_yaml_like(source / "data.yaml")
    channels = int(yaml_payload.get("channels", 3))
    names = yaml_payload.get("names", [])
    items = _collect_items(source)
    if not items:
        raise ValueError(f"No YOLO items found under {source}")

    split = _load_or_make_split(
        items=items,
        seed=seed,
        eval_fraction=eval_fraction,
        val_count=val_count,
        split_manifest=split_manifest,
    )
    if write_split_manifest is not None:
        write_split_manifest.parent.mkdir(parents=True, exist_ok=True)
        write_split_manifest.write_text(json.dumps(json_ready(split), indent=2) + "\n")

    train_stats = _write_split(
        destination=destination,
        items=items,
        target_split="train",
        selected_sample_ids=set(split["train_sample_ids"]),
        hard_repeat=hard_repeat,
        small_area=small_area,
        thin_aspect=thin_aspect,
        copy=copy,
    )
    val_stats = _write_split(
        destination=destination,
        items=items,
        target_split="val",
        selected_sample_ids=set(split["val_sample_ids"]),
        hard_repeat=1,
        small_area=small_area,
        thin_aspect=thin_aspect,
        copy=copy,
    )
    data_yaml = (
        f"path: {destination.resolve()}\n"
        "train: images/train\n"
        "val: images/val\n"
        f"channels: {channels}\n"
        f"names: {names}\n"
    )
    (destination / "data.yaml").write_text(data_yaml)
    summary = {
        "source": str(source),
        "destination": str(destination.resolve()),
        "seed": int(seed),
        "eval_fraction": float(eval_fraction),
        "requested_val_count": val_count,
        "split_manifest": str(split_manifest) if split_manifest else None,
        "write_split_manifest": str(write_split_manifest) if write_split_manifest else None,
        "hard_repeat": int(hard_repeat),
        "small_area": float(small_area),
        "thin_aspect": float(thin_aspect),
        "copy": bool(copy),
        "channels": int(channels),
        "names": names,
        "source_item_count": len(items),
        "source_split_counts": _source_split_counts(items),
        "train": train_stats,
        "val": val_stats,
        "train_sample_ids": split["train_sample_ids"],
        "val_sample_ids": split["val_sample_ids"],
    }
    (destination / "summary.json").write_text(json.dumps(json_ready(summary), indent=2) + "\n")
    return summary


def _collect_items(source: Path) -> list[YoloItem]:
    items: list[YoloItem] = []
    seen: set[str] = set()
    for split in ("train", "val"):
        label_dir = source / "labels" / split
        image_dir = source / "images" / split
        for label_path in sorted(label_dir.glob("*.txt")):
            if "_harddup" in label_path.stem:
                continue
            sample_id = _sample_id_from_stem(label_path.stem)
            if sample_id in seen:
                raise ValueError(f"Duplicate sample_id {sample_id} in {source}")
            image_paths = tuple(sorted(image_dir.glob(f"{label_path.stem}.*")))
            if not image_paths:
                raise FileNotFoundError(f"No image file for {label_path}")
            seen.add(sample_id)
            items.append(
                YoloItem(
                    sample_id=sample_id,
                    stem=label_path.stem,
                    source_split=split,
                    image_paths=image_paths,
                    label_path=label_path,
                )
            )
    return sorted(items, key=lambda item: item.sample_id)


def _sample_id_from_stem(stem: str) -> str:
    match = _SAMPLE_ID_RE.match(stem)
    if match:
        return match.group("sample")
    return stem


def _load_or_make_split(
    *,
    items: Sequence[YoloItem],
    seed: int,
    eval_fraction: float,
    val_count: int | None,
    split_manifest: Path | None,
) -> Dict[str, Any]:
    sample_ids = [item.sample_id for item in items]
    if split_manifest is not None:
        payload = json.loads(split_manifest.read_text())
        val_sample_ids = [str(item) for item in payload["val_sample_ids"]]
        missing = sorted(set(val_sample_ids).difference(sample_ids))
        if missing:
            raise ValueError(f"Split manifest has {len(missing)} missing sample IDs; first={missing[:3]}")
    else:
        count = int(val_count) if val_count is not None else round(len(sample_ids) * float(eval_fraction))
        count = max(1, min(count, len(sample_ids) - 1))
        shuffled = list(sample_ids)
        random.Random(int(seed)).shuffle(shuffled)
        val_sample_ids = sorted(shuffled[:count])
    val_set = set(val_sample_ids)
    train_sample_ids = sorted(sample_id for sample_id in sample_ids if sample_id not in val_set)
    return {
        "seed": int(seed),
        "eval_fraction": float(eval_fraction),
        "val_count": len(val_sample_ids),
        "train_count": len(train_sample_ids),
        "train_sample_ids": train_sample_ids,
        "val_sample_ids": sorted(val_sample_ids),
    }


def _write_split(
    *,
    destination: Path,
    items: Sequence[YoloItem],
    target_split: str,
    selected_sample_ids: set[str],
    hard_repeat: int,
    small_area: float,
    thin_aspect: float,
    copy: bool,
) -> Dict[str, Any]:
    dst_images = destination / "images" / target_split
    dst_labels = destination / "labels" / target_split
    dst_images.mkdir(parents=True, exist_ok=True)
    dst_labels.mkdir(parents=True, exist_ok=True)
    source_images = 0
    output_images = 0
    hard_images = 0
    total_box_count = 0
    hard_box_count = 0
    for item in items:
        if item.sample_id not in selected_sample_ids:
            continue
        source_images += 1
        hard, hard_boxes, total_boxes = _is_hard_label(item.label_path, small_area=small_area, thin_aspect=thin_aspect)
        total_box_count += total_boxes
        hard_box_count += hard_boxes
        repeat = hard_repeat if target_split == "train" and hard else 1
        if hard:
            hard_images += 1
        for copy_index in range(repeat):
            suffix = "" if copy_index == 0 else f"_harddup{copy_index:02d}"
            out_stem = f"{item.stem}{suffix}"
            for image_path in item.image_paths:
                _link_or_copy(image_path, dst_images / f"{out_stem}{image_path.suffix}", copy=copy)
            _link_or_copy(item.label_path, dst_labels / f"{out_stem}.txt", copy=copy)
            output_images += 1
    return {
        "source_images": int(source_images),
        "output_images": int(output_images),
        "hard_images": int(hard_images),
        "total_box_count": int(total_box_count),
        "hard_box_count": int(hard_box_count),
        "hard_fraction": float(hard_images / source_images) if source_images else 0.0,
    }


def _is_hard_label(label_path: Path, *, small_area: float, thin_aspect: float) -> tuple[bool, int, int]:
    hard_count = 0
    total = 0
    for line in label_path.read_text().splitlines():
        if not line.strip():
            continue
        total += 1
        _, _, _, w, h = [float(value) for value in line.split()[:5]]
        area = w * h
        aspect = max(w / h if h > 0 else np.inf, h / w if w > 0 else np.inf)
        if area < small_area or aspect > thin_aspect:
            hard_count += 1
    return hard_count > 0, hard_count, total


def _link_or_copy(source: Path, destination: Path, *, copy: bool) -> None:
    if destination.exists() or destination.is_symlink():
        destination.unlink()
    if copy:
        shutil.copy2(source, destination)
    else:
        destination.symlink_to(source.resolve())


def _source_split_counts(items: Sequence[YoloItem]) -> Dict[str, int]:
    counts = {"train": 0, "val": 0}
    for item in items:
        counts[item.source_split] = counts.get(item.source_split, 0) + 1
    return counts


def _read_yaml_like(path: Path) -> Dict[str, Any]:
    try:
        import yaml

        payload = yaml.safe_load(path.read_text()) or {}
        return dict(payload)
    except Exception:
        payload: Dict[str, Any] = {}
        for raw_line in path.read_text().splitlines():
            line = raw_line.split("#", 1)[0].strip()
            if not line or ":" not in line:
                continue
            key, value = line.split(":", 1)
            key = key.strip()
            value = value.strip().strip("'\"")
            if value.startswith("[") and value.endswith("]"):
                payload[key] = [item.strip().strip("'\"") for item in value[1:-1].split(",") if item.strip()]
            else:
                payload[key] = value
        return payload


if __name__ == "__main__":
    raise SystemExit(main())
